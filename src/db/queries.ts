/**
 * 3-1. カフェ一覧・検索取得 (fetchCafesByContext)
 * * [概要]
 * キーワード、または地域フィルタに基づき、表示用データを取得する。
 * * [実行ロジック]
 * - SELECT name, full_address ... WHERE (条件) LIMIT DISPLAY_LIMIT
 * - 別途、同条件での COUNT(*) を実行し、全件数を取得する。
 * * [引数想定]
 * - db: D1Database
 * - options: { region?: string, keyword?: string }
 */

/**
 * 3-2. 都道府県別・集計取得 (getRegionStats)
 * * [概要]
 * 都道府県ごとの店舗数を集計し、店舗数が多い順（降順）で取得する。
 * * [実行ロジック]
 * - GROUP BY prefecture ORDER BY count DESC LIMIT DISPLAY_LIMIT
 * - 初期表示では Cloudflare 位置情報から得た都道府県を優先的に扱う。
 */

/**
 * 3-3. 市区町村別・集計取得 (getCityStats)
 * * [概要]
 * 指定された都道府県内における、市区町村ごとの店舗数を集計・取得する。
 * * [実行ロジック]
 * - WHERE prefecture = ? GROUP BY city ORDER BY count DESC LIMIT DISPLAY_LIMIT
 */

