/**
 * =============================================================================
 * 【Aletheia (アレテイア) - 開発実験場 (Sandbox) / _sandbox/index.ts】
 * =============================================================================
 * ■ 役割と運用思想
 * -----------------------------------------------------------------------------
 * 1. 隔離環境: 本番ロジックを汚さず、新機能や外部連携（API/DB）を単体検証する空間。
 * 2. 資産継承: 成功したコードは `src/services/` や `hooks/` へ昇格させ、
 * ここには「動く最小サンプル（ノウハウ）」のみを残す。
 * 3. 三段階開発: Sandbox(思考) → Proto(検証) → Production(実行) の第一段階。
 * -----------------------------------------------------------------------------
 * * ■ 開発・運用プロトコル（逆引きリファレンス）
 * -----------------------------------------------------------------------------
 * 【重要：反映】 npx wrangler deploy
 * ⇒ GitHubへのPushだけでは Workers は更新されない。変更後は必ずこのコマンドを。
 * 【重要：監視】 npx wrangler tail
 * ⇒ 本番の console.log をリアルタイム追跡。エラー時の「第一の手がかり」。
 * 【DB操作: Local】 npx wrangler d1 execute aletheia-db --local --file=./seed.sql
 * 【DB操作: Remote】npx wrangler d1 execute aletheia-db --remote --file=./seed.sql
 * -----------------------------------------------------------------------------
 * * ■ 現在有効なテストエンドポイント
 * -----------------------------------------------------------------------------
 * - [認証] /sandbox/test01/login : Google OAuth 連携フローの検証
 * - [基盤] /sandbox/test02       : D1データベース(CRUD)の接続・一覧表示検証
 * - [機能] /sandbox/test03系列    : カフェ検索・予約ロジックの段階的試作
 * -----------------------------------------------------------------------------
 */

import { Hono } from 'hono'
import { trimTrailingSlash } from 'hono/trailing-slash'

/**
 * [1. 各テストモジュールのインポート]
 * ---------------------------------------------------------
 * 新しい実験(test04等)を開始する際は、個別のファイルを作成しここで読み込む。
 */
import { test01 } from './test01_google-auth'  // 認証基盤（Google OAuth）
import { test02 } from './test02_db-access'    // データ基盤（D1 Database）
import { test03 } from './test03_cafe'         // 機能試作（v1: 基礎検索）
import { test03_2 } from './test03-2_cafe'     // 機能試作（v2: リスト表示）
import { test03_3 } from './test03-3_cafe'     // 機能試作（v3: 詳細・UI連携）

/**
 * [2. サンドボックス専用の環境変数定義]
 * ---------------------------------------------------------
 * テスト内で使用するDBインスタンスや環境フラグを定義。
 */
type Bindings = {
  aletheia_db: D1Database  // Cloudflare D1
  ENVIRONMENT?: string      // 実行環境（development / production）
}

// sandboxApp という「テスト用サブアプリ」を生成
export const sandboxApp = new Hono<{ Bindings: Bindings }>()

/**
 * [3. グローバル・ミドルウェアの設定]
 * ---------------------------------------------------------
 * URL末尾の「/」の有無による 404 エラーを防止するガードマン。
 */
sandboxApp.use('*', trimTrailingSlash())

/**
 * [4. 各機能のルーティング登録]
 * ---------------------------------------------------------
 * index.tsx から /sandbox としてマウントされるため、
 * 実際のアドレスは http://localhost:8787/sandbox/testXX となる。
 */
sandboxApp.route('/test01', test01) // 認証
sandboxApp.route('/test02', test02) // DBアクセス
sandboxApp.route('/test03', test03) // カフェ(基本)
sandboxApp.route('/test03-2', test03_2) // カフェ(リスト強化)
sandboxApp.route('/test03-3', test03_3) // カフェ(統合検証)

/**
 * =========================================================
 * 【開発者への申し送り：新しいテストを追加する際の手順】
 * =========================================================
 * 1. 冒頭の「1. インポート」に新ファイルを追加。
 * 例: import { test04 } from './test04_new_feature'
 * * 2. 上記の「4. 各機能の登録」にルートを追記。
 * 例: sandboxApp.route('/test04', test04)
 * * 3. ブラウザで動作確認（デプロイ後は tail でログ監視）。
 * =========================================================
 */